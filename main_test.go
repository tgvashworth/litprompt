// --- config-driven build (runBuildFromConfig) ---

func TestRunBuildFromConfig_glob_siblingOutput_withHeader(t *testing.T) {
	dir := t.TempDir()
	t.Chdir(dir)

	writeTestFile(t, "plugins/data/skills/query/SKILL.src.md", "# query\n")
	writeTestFile(t, "plugins/data/skills/chart/SKILL.src.md", "# chart\n")
	writeTestFile(t, "litprompt.yaml", `builds:
  - source: plugins/*/skills/*/SKILL.src.md
    output: SKILL.md
    header: full
`)

	if err := runBuildFromConfig(build.Options{}); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	for _, name := range []string{"query", "chart"} {
		out := mustRead(t, filepath.Join("plugins/data/skills", name, "SKILL.md"))
		if !strings.Contains(out, "# "+name) {
			t.Errorf("%s output missing content: %q", name, out)
		}
		if !strings.Contains(out, "Generated by litprompt") {
			t.Errorf("%s output missing header: %q", name, out)
		}
	}
}

func TestRunBuildFromConfig_directoryMode(t *testing.T) {
	dir := t.TempDir()
	t.Chdir(dir)

	writeTestFile(t, "src/a.md", "# a\n")
	writeTestFile(t, "src/sub/b.md", "# b\n")
	writeTestFile(t, "litprompt.yaml", `builds:
  - source: src/
    output: out/
`)

	if err := runBuildFromConfig(build.Options{}); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	if got := mustRead(t, "out/a.md"); !strings.Contains(got, "# a") {
		t.Errorf("out/a.md missing content: %q", got)
	}
	if got := mustRead(t, "out/sub/b.md"); !strings.Contains(got, "# b") {
		t.Errorf("out/sub/b.md missing content: %q", got)
	}
}

func TestRunBuildFromConfig_keepGoingOnError(t *testing.T) {
	dir := t.TempDir()
	t.Chdir(dir)

	writeTestFile(t, "src/good.md", "# good\n")
	writeTestFile(t, "src/bad.md", "@[missing](./does-not-exist.md)\n")
	writeTestFile(t, "litprompt.yaml", `builds:
  - source: src/
    output: out/
`)

	err := runBuildFromConfig(build.Options{})
	if err == nil {
		t.Fatal("expected error from failing build, got nil")
	}
	if !strings.Contains(err.Error(), "failed") {
		t.Errorf("error should mention failure: %v", err)
	}
	if _, statErr := os.Stat("out/good.md"); statErr != nil {
		t.Errorf("good output should still be written despite a sibling failure: %v", statErr)
	}
	if _, statErr := os.Stat("out/bad.md"); statErr == nil {
		t.Errorf("bad output should NOT exist")
	}
}

