func TestRunBuildFromConfig_mixedShapes(t *testing.T) {
	dir := t.TempDir()
	t.Chdir(dir)

	writeTestFile(t, "single.md", "# single\n")
	writeTestFile(t, "src/a.md", "# a\n")
	writeTestFile(t, "skills/foo/SKILL.src.md", "# foo skill\n")
	writeTestFile(t, "litprompt.yaml", `builds:
  - source: single.md
    output: dist/single-out.md
  - source: src/
    output: out/
  - source: skills/*/SKILL.src.md
    output: SKILL.md
`)

	if err := runBuildFromConfig(build.Options{}); err != nil {
		t.Fatalf("unexpected error: %v", err)
	}

	for _, p := range []string{"dist/single-out.md", "out/a.md", "skills/foo/SKILL.md"} {
		if _, err := os.Stat(p); err != nil {
			t.Errorf("expected %s to exist: %v", p, err)
		}
	}
}
